package challenges.interviews;

public class MinTICEJemplo {

	public static void main(String[] args) {
		int x = 0, y = 0;
		// operador ejecuta operacion y luego asigna
		System.out.println(x++);
		System.out.println(x);
		
		// operador asigna y luego ejecuta
		System.out.println(--y);
		System.out.println(y);
		
		System.out.println(~y);
		System.out.println(y);
	}
}
